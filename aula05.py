# Tipo de dado bool (boolean) 
# (True) ou (False) 
# ( == ) para questionar se um valor é igual ao outro

print(10 == 10) # O terminal vai retornar como True, pois eu coloquei dois valores iguais para comparar
print(10 == 15) # O terminal vai retornar como False, pois eu coloquei dois valores difetentes para comparar

print(type(True))
print(type(False))

print(type(10 == 10)) # Usando a class type() para dizer qual é o tipo de variável que estou usando
print(type(10 == 15)) # A variável é do mesmo tipo, independente ser for True ou False, ainda é booleana