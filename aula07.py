# Criando uma variável
# Devemos iniciar com letras minúsculas, e podemos usar números e underline
# O sinal ( = ) é o operador que atribui o valor da variável 

nome_completo = "Felipe Sampaio" # Criei a variável "nome_completo" com o valor "Felipe Sampaio"
idade = 24
maior_de_idade = idade >= 18

# Se a variável "idade" for igual ou maior de 18, a variável "maior_de_idade" vai retornar True
# Se eu alterar essa variável "idade" como menos de 18, a variável "maior_de_idade" vai retornar False

print("Meu nome é:", nome_completo, "tenho", idade, "anos de idade")
print("Sou maior de idade?", maior_de_idade)




