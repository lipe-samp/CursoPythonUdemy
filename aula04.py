# Tipo int e float

# int = número inteiro
# float = número com ponto flutuante 

print(-4, -3, -2, -1, 0, 1, 2, 3, 4) # int

print(-4.2, -3.5, -2.7, -1.3, 0.1, 1.9, 2.5, 3.4, 4.5) # float

# Fazendo com que o terminal me diga qual tipo de valor:
# Usando type() dentro do print()

print(type("Hello, world!")) # o terminal vai me dizer que é do tipo STRING
print(type(1)) # o terminal vai me dizer que é do tipo INT
print(type(9.2)) # o terminal vai me dizer que é do tipo FLOAT

print(type("Hello, world!"), type(100), type(5.5)) # Agora aplicando todos os tipos de valores em um único print